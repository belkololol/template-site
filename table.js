let menuList = document.querySelectorAll('.menu-list ul li');
let table = document.querySelector('.table');


let indexArray = 0;

function createMenu() {

    let containerList = document.createElement('div');
    let name = document.createElement('p');
    let cost = document.createElement('p');
    let description = document.createElement('p');


    containerList.classList.add('container-list')
    description.classList.add('description-item')

    for (obj of menu[indexArray]) {
        name.innerHTML = obj.dish + ' ......';
        containerList.appendChild(name);
        cost.innerHTML = obj.cost;
        containerList.appendChild(cost);
        description.innerHTML = obj.description;
        containerList.appendChild(description);
        table.appendChild(containerList.cloneNode(true));

    }



}

createMenu()



menuList.forEach((item) => {
    item.addEventListener('click', () => {
        let div = document.querySelectorAll('.container-list');
        for (let i = 0; i < div.length; i++) {
            table.removeChild(div[i]);
        }
        createMenu()
    })
})

